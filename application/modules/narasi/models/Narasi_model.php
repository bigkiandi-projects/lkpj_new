    // datatables
        function json() {
            $this->datatables->select('id_narasi,unit_kerja,urusan,uraian_kegiatan');
            $this->datatables->from('narasi');
            $this->datatables->where('tahun', $this->session->userdata('ta'));
        //add this line for join
        //$this->datatables->join('table2', 'narasi.field = table2.field');
            $this->datatables->add_column('action', 
            '<a href="'  . site_url('narasi/read/$1') . '" class="btn btn-info"><i class="fa fa-eye"></i></a> 
            <a href="'  . site_url('narasi/update/$1') . '" class="btn btn-warning"><i class="fa fa-edit"></i></a> 
            <a data-href="'  . site_url('narasi/delete/$1') . '" class="btn btn-danger hapus-data"><i class="fa fa-trash"></i></a>', 'id_narasi');
            return $this->datatables->generate();
        }


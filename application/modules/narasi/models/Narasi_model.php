        $this->db->like('id_narasi', $q);
	$this->db->or_like('unit_kerja', $q);
	$this->db->or_like('urusan', $q);
	$this->db->or_like('uraian_kegiatan', $q);
	$this->db->from($this->table);
        return $this->db->count_all_results();
    }

